<br>
 

<b>Sitios Web de Concesionarios</b>
<br>

Muchos restaurantes de McDonald's pertenecen y son operados por concesionarios ("franchises"), quienes son empresarios(as) independientes. Algunos de los concesionarios también operan sitios Web. En muchos casos, los sitios Web de los concesionarios siguen esta política de privacidad. Sin embargo, por favor, revise la política de privacidad de cada sitio Web en particular, para determinar las políticas de privacidad de ese sitio.
