<b>Otros Sitios Web de McDonald's</b>
<br>

Todos los sitios Web de McDonald's que son operados por McDonald's Corporationcuentan con una política de privacidad. No obstante, las políticas de algunos de los sitios Web de McDonald's podrían variar debido a las costumbres, prácticas o leyes locales, o debido a las circunstancias particulares de ese sitio. Sin embargo, en todos los casos, los sitios operados por McDonald's Corporation honrarán los compromisos con nuestros clientes enumerados en esta política relacionados a la recolección y uso de información personal.
