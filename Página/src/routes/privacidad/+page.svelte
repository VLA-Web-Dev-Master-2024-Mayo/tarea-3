<b>Cambios a nuestra Política de Privacidad</b>
<br>

Esta política de privacidad es válida y eficaz a partir de octubre de 2021. En ocasiones podría ser necesario para McDonald's cambiar o modificar esta política de privacidad. Si cambiamos nuestra política, publicaremos la versión revisada de la misma manera y requeriremos de su consentimiento.  Tenga la tranquilidad, sin embargo, de que ningún cambio será aplicado retroactivamente y que no alterará la forma cómo manejamos la información previamente recolectada.
