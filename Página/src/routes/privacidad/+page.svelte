<b>Enlaces a otros Sitios</b>
<br>

Podríamos ofrecer enlaces ("links") a sitios Web que no son operados por McDonald's. Si usted visita uno de esos sitios Web enlazados, debe revisar su política de privacidad y cualquier otra política que pueda tener. No nos hacemos responsables de las políticas y las prácticas de otras compañías, y cualquier información que usted someta a esas compañías está conforme a sus políticas de aislamiento.
