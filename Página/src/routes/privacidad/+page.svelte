<b>Nuestra Política de Privacidad con respecto a Menores</b>
<br>

McDonald's es muy consciente de la importancia de las cuestiones de privacidad. En ese sentido, estamos orgullosos de nuestro prolongado compromiso con nuestros clientes. Somos especialmente cuidadosos en cualquier comunicación con uno de nuestros clientes más atesorados - los niños. 

En nuestras páginas web, ofrecemos muchas atracciones, como juegos y libros colorear, que no requieren que el menor proporcione información personal alguna. Recolectamos solamente información personal limitada de menores hasta los 12 años de edad (por ejemplo, sus direcciones de correo electrónico), para poder responder a los requerimientos que nos hacen por la página web. Por ejemplo, podríamos recolectar el correo electrónico de un niño/a para enviarle un protector de pantalla ("screensaver"), pero borramos la dirección de nuestro sistema lo antes posible, o podríamos recoger la dirección de correo electrónico de un menor para inscribirlo en uno de nuestros concursos (si así lo solicitase el cliente). Podríamos también pedirle a un menor que provea la dirección de correo electrónico de uno de sus padres para poder notificarle y/o solicitar su consentimiento. 

No recogeremos información más detallada de un menor de 12 años, tal como una dirección postal o un número de teléfono, sin el consentimiento de uno de sus padres o tutor. 

McDonald's, bajo ninguna circunstancia, condicionará la participación de un menor en ninguna actividad en-línea a la divulgación por el menor de más información personal de la que sea razonablemente necesaria para participar en dicha actividad. 

La información personal recogida de los menores es utilizada únicamente por McDonald's u otras entidades que proporcionan servicios técnicos, de cumplimiento u otros servicios a McDonald's, en los mismos términos indicados en el apartado anterior. 

 Esta información personal no se vende o transfiere a terceros. Instamos a los padres a que regularmente vigilen y supervisen las actividades en Internet que realizan sus hijos.
