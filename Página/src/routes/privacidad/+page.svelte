<b>Sobre el uso de "cookies"</b>
<br>

Las  "cookies" son las porciones de información que un sitio en Internet transfiere al disco duro de un individuo con el propósito de mantener registros de su visita. Las  "cookies" permiten que el sitio en Internet recuerde información importante que hará su uso del sitio más conveniente. 

Como la mayoría de los sitios en Internet, McDonald's utiliza  "cookies" para una variedad de propósitos que mejoren su experiencia en Internet. Por ejemplo, rastreamos el número total de visitantes a nuestro sitio de manera anónima. Podríamos también emplear los "cookies" de modo que le recordemos cuando usted vuelve al sitio, para mantener un registro de los artículos en su carro de compras si usted compra mercancía en-línea, para administrar ciertos concursos y promociones y para ayudarnos a personalizar su experiencia en el sitio webde McDonald's. Podríamos asociar la información personal a un fichero con  "cookies" en esos casos. 

Utilice las opciones en su navegador de Internet si usted no desea recibir una"cookie" o si usted desea ajustar su navegador para que le notifique cuando usted recibe una "cookie". Haga clic en la sección de "Ayuda" de su navegador para aprender cómo cambiar sus preferencias del uso de  "cookies". Si inhabilita todas las "cookies", usted podría no beneficiarse de todas las funciones del sitio en Internet.
