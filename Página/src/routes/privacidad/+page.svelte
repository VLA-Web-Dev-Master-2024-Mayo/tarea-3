<b>Tipos de Información que Recogemos y Cómo se Utiliza</b>
<br>

McDonald's hace de su conocimiento que en caso de que así usted lo conscienta, McDonald’s recolectará su información personal, como nombres y apellidos, direcciones físicas y direcciones de correo electrónico ("e-mail"), la cual será almacenada en la base de datos de carácter personal de McDonald´s. 

La información será recolectada con ocasión de su acceso a nuestra página web, para su uso, o bien, para brindar su opinión sobre nuestra página web, nuestros productos,  para inscribirle en uno de nuestros concursos o promociones, o bien, para la atención de alguna consulta o reclamo.Esta información personal es voluntariamente enviada por usted. 

Se hace de su conocimiento que McDonald's, para el cumplimiento de los fines señalados anteriormente, podrá contratar a otras entidades para que le provean una variedad de servicios, tales como, llenar ordenes, ayudar con promociones, prestar servicios técnicos a nuestra página web, o para colaborar con las funciones propias de McDonald´s,  por lo que, podrá transferir sus datos personales a estas entidades contratadas, Estas entidades sólo usarán esa información personal para cumplir con la misión encomendada y no la podrán usar para ningún otro propósito o fin, sin poder tampoco, transferirla a terceros. 

McDonald's no venderá, transferirá o divulgará información personal a terceros fuera de la Familia McDonald's, salvo la transferencia de datosseñalada en el párrafo anterior. La Familia McDonald’s incluye a McDonald’s Corporation, sus franquiciass, afiliadas y/o subsidiarias. Sin embargo, con su permiso, le enviaremos a usted de manera ocasional cierta información de mercadeo sobre alguno de nuestros socios o sobre los productos o servicios que éstos proveen y que pueda serle de interés. 
<br>
<br>
 

Puede que se le pregunte si usted desea recibir materiales de mercadeo de los socios de McDonald's. Si usted escoge recibir dichos materiales, McDonald's no compartirá su información personal con tales socios sino que le enviará una carta o un correo electrónico a nombre de los socios. 

McDonald's tratará los datos de acuerdo con los términos señalados en la presente política, sin poder transferirlos a terceros, salvo los supuestos señalados anteriormente. Asimismo, se  reserva el derecho a usar o divulgar toda información necesaria para cumplir con cualquier requisito impuesto por la legislación costarricense. 

Se hace de su conocimiento que, en caso de no suministrar sus datos personales según lo requerido, no podrá hacer uso de los distintos programas, aplicaciones o participar en las promociones que McDonald´s pone a su disposición a través de la presente página web. 

McDonald´s hace de su conocimiento que puede ejercer, en los términos que establece la Ley de Protección de la persona frente al tratamiento de sus datos personales, los derechos de acceso, rectificación y cancelación, ante servicio al cliente de McDonald´s al correo electrónico servicio.cliente@cr.mcd.com con el fin de corregir la información personal que tenemos sobre usted o si quiere cambiar sus preferencias sobre los contactos que hacemos nosotros o a nombre de nuestros socios, déjenoslo saber comunicándose con nosotros a través del correo electrónico o la dirección postal especificada al final de la presente política. Asimismo, y en caso de querer revisar cualquier información personal que nosotros tengamos sobre usted o solicitar que borremos dicha información o que descontinuemos el uso futuro de dicha información. 

Asimismo, se asegura al usuario que McDonald’s se adhiere y cumple a cabalidad las disposiciones contenidas en la normativa costarricense vigente sobre protección de datos de carácter personal.
